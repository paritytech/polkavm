#![no_std]
#![no_main]

include!("../../bench-common.rs");

struct State;
define_benchmark! {
    heap_size = 0,
    state = State,
}

use ed25519_dalek::{Signature, Verifier, VerifyingKey};

const PUBLIC_KEY: [u8; 32] = [
    0xfc, 0x51, 0xcd, 0x8e, 0x62, 0x18, 0xa1, 0xa3, 0x8d, 0xa4, 0x7e, 0xd0, 0x02, 0x30, 0xf0, 0x58, 0x08, 0x16, 0xed, 0x13, 0xba, 0x33,
    0x03, 0xac, 0x5d, 0xeb, 0x91, 0x15, 0x48, 0x90, 0x80, 0x25,
];

const SIGNATURE: [u8; 64] = [
    0x62, 0x91, 0xd6, 0x57, 0xde, 0xec, 0x24, 0x02, 0x48, 0x27, 0xe6, 0x9c, 0x3a, 0xbe, 0x01, 0xa3, 0x0c, 0xe5, 0x48, 0xa2, 0x84, 0x74,
    0x3a, 0x44, 0x5e, 0x36, 0x80, 0xd7, 0xdb, 0x5a, 0xc3, 0xac, 0x18, 0xff, 0x9b, 0x53, 0x8d, 0x16, 0xf2, 0x90, 0xae, 0x67, 0xf7, 0x60,
    0x98, 0x4d, 0xc6, 0x59, 0x4a, 0x7c, 0x15, 0xe9, 0x71, 0x6e, 0xd2, 0x8d, 0xc0, 0x27, 0xbe, 0xce, 0xea, 0x1e, 0xc4, 0x0a,
];

const MESSAGE: [u8; 2] = [0xaf, 0x82];

fn benchmark_initialize(_state: &mut State) {}

fn benchmark_run(_state: &mut State) {
    use core::hint::black_box;

    let key = VerifyingKey::from_bytes(black_box(&PUBLIC_KEY)).unwrap();
    let signature = Signature::from_bytes(black_box(&SIGNATURE));
    black_box(key.verify(&MESSAGE, &signature)).unwrap();
}
